#example of the listed unicode characters
international_greeting = "\u4f60\u597d"  # "你好" in Chinese
print(international_greeting)  # Output: 你好
#unicode for special symbols
check_mark = "\u2713"  # Check mark symbol
print(check_mark)  # Output: ✓
#emojis
smiley_face = "\U0001F600"  # Grinning face emoji
print(smiley_face)  # Output: 😀
#special symbols
heart_symbol = "\u2764"  # Heart symbol
print(heart_symbol)  # Output: ❤
#math symbols
infinity_symbol = "\u221E"  # Infinity symbol
print(infinity_symbol)  # Output: ∞
#currency symbols
euro_symbol = "\u20AC"  # Euro symbol
print(euro_symbol)  # Output: €
#arrows
right_arrow = "\u2192"  # Right arrow symbol
print(right_arrow)  # Output: →
#miscellaneous symbols
star_symbol = "\u2605"  # Star symbol
print(star_symbol)  # Output: ★
#accents and diacritics
accented_a = "\u00E1"  # "á" character
print(accented_a)  # Output: á
#mathematical operators
plus_minus = "\u00B1"  # Plus-minus symbol  
print(plus_minus)  # Output: ±
#fractions
one_half = "\u00BD"  # One-half symbol
print(one_half)  # Output: ½
#superscripts and subscripts
superscript_two = "\u00B2"  # Superscript two symbol
print(superscript_two)  # Output: ²
#greek letters
alpha = "\u03B1"  # Greek letter alpha
print(alpha)  # Output: α
#cyrillic letters
cyrillic_a = "\u0430"  # Cyrillic letter a
print(cyrillic_a)  # Output: а
#arabic letters
arabic_alif = "\u0627"  # Arabic letter alif
print(arabic_alif)  # Output: ا 
#arabic for maheen
arabic_maheen = "\u0645\u0647\u064A\u0646"  # "مهين" in Arabic
print(arabic_maheen)  # Output: مهين